    private void bntPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntPesquisarActionPerformed
        // TODO add your handling code here:
        
        
          String nome = txtCategoria.getText();
        DefaultTableModel tableModel = (DefaultTableModel) tblCategoria.getModel(); // SEMPRE QUE CRIAR UMA TABELA, PRECISA INSTANCIAR
        tableModel.setRowCount(0); // zera as linhas da tabela, sempre que fizer uma pesquisa, se nao colocar ele deixa todas as linhas das consultas interiores, se colocar 2 no lugar do 0 ele deixa 2
        CategoriaController catCont = new CategoriaController();
        
        
     try {
           ArrayList<Categorias> categorias = catCont.listarCategoria(nome);
            categorias.forEach((Categorias categoria) -> {
                tableModel.addRow(new Object[] {categoria.getId(),categoria.getNome()});
                
            });
            
        } catch (Exception e) {
            
            
        }
        
    }//GEN-LAST:event_bntPesquisarActionPerformed

