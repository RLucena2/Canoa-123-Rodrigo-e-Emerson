    private void tblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriaMouseClicked
        
      
            if(evt.getClickCount() == 1){
           // abaixo vai pegar o valor que ta na coluna zero
            Integer categoriaid = (Integer) tblCategoria.getModel().getValueAt(tblCategoria.getSelectedRow(), 0);
            String nome = (String) tblCategoria.getModel().getValueAt(tblCategoria.getSelectedRow(), 1);
           
            this.telaCadastroCategoria.buscarCategoria(categoriaid, nome);
            this.telaCadastroCategoria.setVisible(true);
            this.dispose();

        }
    }//GEN-LAST:event_tblCategoriaMouseClicked

